    private void buttonGuardarMatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonGuardarMatActionPerformed
        try {
            if (textNombreMat.getText().isEmpty() || textA単oMat.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Le falta ingresar datos");
            } else {
                int a単o = Integer.parseInt(textA単oMat.getText());
                Materia materia = new Materia(textNombreMat.getText(), a単o, buttonEstadoMat.isSelected());
                mat.guardarMateria(materia);
            }
        } catch (NumberFormatException nf) {
            JOptionPane.showMessageDialog(null, "Caracteres ingresado en a単o son invalidos");
        }
    }//GEN-LAST:event_buttonGuardarMatActionPerformed

